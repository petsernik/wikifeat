from dataclasses import dataclass
from typing import List, Optional


@dataclass
class Image:
    """
    Структура данных, описывающая изображение и связанную с ним
    лицензионную и атрибуционную информацию.

    Используется для хранения метаданных изображения, полученных
    со страницы Википедии или другого источника.
    """
    desc: str
    """Описание изображения: file_id фотографии из telegram или http url или путь к изображению."""

    licenses: List[str]
    """Список лицензий изображения (например: ['CC-BY-SA', 'GFDL'])."""

    page_url: str
    """URL страницы, на которой размещена дополнительная информация об изображении."""

    author_html: str
    """HTML-фрагмент с информацией об авторе."""


@dataclass
class Article:
    """
    Структура данных, представляющая статью с текстовым содержимым
    и опционально связанным изображением.

    Используется для хранения преамбулы статьи, полученной из Википедии
    или другого текстового источника.
    """

    title: str
    """Заголовок статьи."""

    paragraphs: List[str]
    """Список абзацев статьи в порядке следования."""

    link: str
    """URL статьи или каноническая ссылка на источник."""

    image: Optional[Image]
    """Связанное с статьёй изображение или None, если изображения нет."""
